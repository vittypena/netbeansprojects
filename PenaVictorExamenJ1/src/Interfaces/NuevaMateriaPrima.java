    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Insertar Materia        
        String simbolo = jTextField1.getText();
        String nombre = jTextField2.getText();
        String p_ventaString = jTextField3.getText();
        Double p_venta = Double.parseDouble(p_ventaString);
        String p_compraString = jTextField4.getText();
        Double p_compra = Double.parseDouble(p_compraString);        
        materia materiaIn = null;
//        GestorArchivos.escribirFichero(simbolo, nombre);
        try {            
            materiaIn = new materia(simbolo, nombre, p_venta, p_compra);
            listaAñadidos.add(materiaIn);
        } catch (Excepciones ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());   
        }
        try {                                         
            //Confirmar alta, lo inserta en la base de datos
            try {
                conexion = Conexion.getConnection();
            } catch (Excepciones ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            GestorBaseDatos cuentaDao = new GestorBaseDatos(conexion);
            cuentaDao.insertar(materiaIn);
        } catch (SQLException | Excepciones ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());   
        }
        //Volver a poner los campos vacios
        camposVacios();
    }//GEN-LAST:event_jButton1ActionPerformed
    
